#include "filler.h"

void	player(t_player *p)
{
	char *line;

	line = 0;
	get_next_line(STDIN_FILENO, &line);
	p->player = (line[10] == '1' ? 'o' : 'x');
	p->enemy = (line[10] == '1' ? 'x' : 'o');
}
