#include "filler.h"

int		p_atoi(char **line)
{
	int num;

	num = 0;
	while (**line == ' ')
		(*line)++;
	while (**line >= '0' && **line <= '9')
	{
		num = num * 10 + (**line - '0');
		(*line)++;
	}
	return (num);
}
